package logic;

public class Salesmen {
private int id;
private String name;
private boolean chef;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public boolean isChef() {
	return chef;
}
public void setChef(boolean chef) {
	this.chef = chef;
}


}
